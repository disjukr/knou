par(cex = 4)

co2 <- c(
  450, 520, 480, 550, 510,
  610, 470, 530, 570, 500,
  580, 610, 490, 550, 420,
  580, 440, 520, 600, 560,
  410, 540, 590, 530, 490,
  570, 610, 470, 540, 590,
  510,
)

freq_table <- table(cut(co2, breaks = seq(400, 600, by = 50)))
print(freq_table)

barplot(
  freq_table,
  main = "대기 중 이산화탄소 농도 분포",
  xlab = "이산화탄소 농도(ppm)",
  ylab = "빈도",
  names.arg = c("400-450", "450-500", "500-550", "600-650")
)
